class Personnel {
    
    private idPerso: number;
	private nom: string;
	private prenom: string;
	private adresse: string;
	private poste: string;
	
	constructor(idPerso, nom, prenom, adresse, poste){
		this.idPerso = idPerso;
		this.nom = nom;
		this.prenom = prenom;
		this.adresse = adresse;
		this.poste = poste;
	}
 }